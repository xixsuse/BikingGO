package com.kingwaytek.cpami.bykingTablet.app.rentInfo;

public class DistenceObject {
	private double dis;
	private int sno;

	public DistenceObject(double dis, int sno) {
		this.dis = dis;
		this.sno = sno;
	}

	public double getDis() {
		return this.dis;
	}

	public int getSno() {
		return this.sno;
	}
}
